-- Script para agregar contenido a Romeo y Julieta
USE libronet;

-- Actualizar "Romeo y Julieta"
UPDATE libros 
SET contenido_completo = 'Dos familias, iguales en dignidad,
en la hermosa Verona, donde ocurre nuestra escena,
de antiguos rencores rompen la paz,
y la sangre civil mancha las manos civiles.
De los lomos fatales de estos dos enemigos
nace una pareja de amantes malditos,
cuyos lamentables y piteosos naufragios
hacen morir con su muerte la discordia de sus padres.
El temeroso pasaje de su amor marcado por la muerte,
y la continuación de la ira de sus padres,
que, salvo la muerte de sus hijos, nada pudo terminar,
es ahora el tráfico de dos horas de nuestra escena;
el cual, si escucháis con oídos pacientes,
lo que aquí falta, nuestro trabajo se esforzará por enmendar.

ESCENA I
Una plaza pública en Verona.

Entran SANSÓN y GREGORIO, criados de los Capuleto, armados con espadas y escudos.

SANSÓN
Gregorio, por mi palabra, no llevaremos carbón.

GREGORIO
No, porque entonces seríamos carboneros.

SANSÓN
Quiero decir, si estamos de mal humor, sacaremos las espadas.

GREGORIO
Sí, mientras vivas, saca el cuello del yugo.

SANSÓN
Me pongo de mal humor cuando me provocan.

GREGORIO
Pero no te pones de mal humor para provocar.

SANSÓN
Un perro de la casa de Montesco me pone de mal humor.

GREGORIO
Mover significa irse; ser valiente significa quedarse: por lo tanto, si te pones de mal humor, te vas.

SANSÓN
Un perro de esa casa me pondrá de mal humor para quedarme. Tomaré la pared de cualquier hombre o doncella de los Montesco.

GREGORIO
Eso muestra que eres un esclavo débil; porque los más débiles van a la pared.

SANSÓN
Es cierto; y por eso las mujeres, siendo las criaturas más débiles, son empujadas hacia la pared. Por lo tanto, empujaré a los hombres de los Montesco de la pared, y empujaré a sus doncellas hacia la pared.

GREGORIO
La disputa es entre nuestros amos y nosotros sus hombres.

SANSÓN
Es lo mismo. Seré tan cruel como un tirano: cuando haya luchado con los hombres, seré cruel con las doncellas; les cortaré la cabeza.

GREGORIO
¿Las cabezas de las doncellas?

SANSÓN
Sí, las cabezas de las doncellas, o sus cabezas de doncella; tómalo en cualquier sentido que quieras.

GREGORIO
Las doncellas lo sentirán según lo que sientan.

SANSÓN
Me sentirán mientras pueda mantenerme en pie; y se sabe que soy un pedazo de carne duro.

GREGORIO
Es bueno que seas pesado; de lo contrario, serías un pescado. Toma tu herramienta: aquí vienen dos de la casa de los Montesco.

Entran ABRAHAM y BALTASAR.

SANSÓN
Mi herramienta está desenvainada: pelea; te respaldo.

GREGORIO
¿Cómo? ¿Das la espalda y huyes?

SANSÓN
No temas por mí.

GREGORIO
No, por Dios; pero temo por ti.

SANSÓN
Déjame pelear con estos hombres: no te metas.

GREGORIO
¿Qué, pelear? No, te digo.

SANSÓN
Pero si me provocan, ¿no debo responder?

ABRAHAM
¿Me provocas, señor?

SANSÓN
¡Señor! No, señor; pero sí, señor, si Dios me salve.

ABRAHAM
¿Me provocas, señor?

SANSÓN
[Aparte a GREGORIO] ¿La ley está de nuestro lado si digo sí?

GREGORIO
[Aparte a SANSÓN] No.

SANSÓN
No, señor, no os provoco, señor; pero sí, señor, si Dios me salve.

ABRAHAM
¿Me provocas, señor?

SANSÓN
¡Sí, señor! ¿O no? Decid vos.

GREGORIO
[Aparte a SANSÓN] Di "sí": aquí viene uno de mis parientes.

SANSÓN
Sí, señor.

ABRAHAM
¡Mientes!

SANSÓN
Sacad las espadas, si sois hombres. Gregorio, recordad vuestro golpe de estocada.

Luchan.

Entra BENVOLIO.

BENVOLIO
¡Apartaos, necios! ¡Poned las espadas en su lugar! No sabéis lo que hacéis.

Entra TEBALDO.

TEBALDO
¿Qué, estás mezclado entre estos criados sin corazón?
¡Vuelve, Benvolio, mira tu muerte!

BENVOLIO
No hago más que mantener la paz: pon tu espada,
o úsala para ayudarme a separar a estos hombres.

TEBALDO
¿Qué, espada en mano, y hablas de paz? ¡Odio la palabra,
como odio el infierno, todos los Montesco, y a ti!
¡Toma, cobarde!

Luchan.

Entran varios ciudadanos armados con palos y lanzas.

CIUDADANO PRIMERO
¡Palos, lanzas! ¡Golpeadlos! ¡Abajo con los Capuleto! ¡Abajo con los Montesco!

Entran CAPULETO en bata, y la SEÑORA CAPULETO.

CAPULETO
¿Qué ruido es este? ¡Dadme mi espada larga, eh!

SEÑORA CAPULETO
¡Una muleta, una muleta! ¿Por qué pedís una espada?

CAPULETO
¡Mi espada, digo! ¡Viene el viejo Montesco,
y blande su acero a pesar de mi edad!

Entran MONTESCO y la SEÑORA MONTESCO.

MONTESCO
¡Villano Capuleto! ¡Déjame pasar!

SEÑORA MONTESCO
¡No darás un paso más, villano!

Entra el PRÍNCIPE con su séquito.

PRÍNCIPE
¡Rebeldes, enemigos de la paz,
profanadores de esta sangre de hierro!
¿No oirán? ¿Qué, hombres! ¡Bestias,
que apagáis el fuego de vuestra perniciosa rabia
con chorros de sangre púrpura!
Si no me obedecéis, os haré quebrar vuestros miembros
con torturas. ¡Manos arriba, todos!
¡Escuchad lo que vuestro príncipe dice!
Tres riñas civiles, nacidas de una palabra vana,
por ti, viejo Capuleto, y por ti, Montesco,
han perturbado tres veces las calles tranquilas de Verona,
y han hecho que los ciudadanos de Verona,
que llevan sus mejores galas, quiten sus mejores galas
y tomen sus viejas armas oxidadas,
para romper la concordia de larga duración
de calles antiguas con sangre de calles nuevas.
Si alguna vez perturbáis nuestras calles de nuevo,
vuestras vidas pagarán la paz que habéis roto.
Por esta vez, que todos los demás se vayan:
tú, Capuleto, vendrás conmigo;
y tú, Montesco, vendrás esta tarde
a conocer nuestra sentencia más lejana
en el antiguo castillo libre de la ciudad.
Una vez más, ¡so pena de muerte, que todos se vayan!

[Salen todos excepto MONTESCO, SEÑORA MONTESCO y BENVOLIO.]

MONTESCO
¿Quién ha reavivado esta antigua pelea?
Habla, sobrino, ¿estabas presente cuando comenzó?

BENVOLIO
Aquí estaban los criados de vuestro adversario
y los vuestros, antes de que llegara yo:
ya estaban en lucha cuando los separé;
en ese momento llegó el fogoso Tebaldo con su espada,
que blandía contra mi pecho, y cortaba el viento,
que, sin hacerle daño, silbaba en vano.
Mientras intercambiábamos golpes, llegó
más gente que se unió a la pelea,
hasta que el príncipe vino y separó a todos.

SEÑORA MONTESCO
¿Dónde está Romeo? ¿Lo has visto hoy?
¡Qué alegría me da que no estuviera en esta pelea!

BENVOLIO
Señora, una hora antes de que el sol
dorara la ventana del este de esa ventana,
mi mente inquieta me llevó a pasear,
y bajo la sombra de un sicómoro
que se extiende hacia el oeste de la ciudad,
vi a vuestro hijo temprano en la mañana
paseando por allí. Me dirigí hacia él,
pero tan pronto como me vio, se escondió
en el bosque. Yo, midiendo sus afectos por los míos,
que eran los más ocupados buscando los míos,
y siendo yo mismo el más ocupado en evitarme,
seguí mi propio placer y me aparté de él,
y me alegré de que él fuera tan discreto
como para huir de mí que lo buscaba.'
WHERE titulo = 'Romeo y Julieta'; 